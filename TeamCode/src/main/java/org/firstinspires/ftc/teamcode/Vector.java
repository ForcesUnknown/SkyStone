package org.firstinspires.ftc.teamcode;

public class Vector
{
    public double x;
    public double y;
    public double z;

    public Vector(double x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public Vector(double x, double y, double z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
